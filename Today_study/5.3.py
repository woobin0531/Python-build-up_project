# 문제 1: 오픈 프레임만 있는 경우
# 요구사항:
# 10프레임 모두 오픈 프레임(스트라이크/스페어 없음)일 때 점수 계산
# 예시 입력: [3,4, 5,2, 7,1, ..., 8,1] (총 20개 숫자)
# 출력: 각 프레임 점수의 합
# 정답 예시:
# 입력: [3,4, 5,2, 7,1, 6,3, 2,5, 4,4, 3,2,][3][2][8][1][9][2][2]
# 출력: 3+4 +5+2 +7+1 +6+3 +2+5 +4+4 +3+2 +8+1 +0+9 +2+2 = 76

def 오픈프레임_점수계산(rolls):
  프레임별점수 = []
  총점 = 0
  for i in range(0, 20, 2):
    프레임점수 = rolls[i] + rolls[i+1]
    프레임별점수.append(프레임점수)
    총점 += 프레임점수
  print("총점:", 총점)
  return 프레임별점수

rolls = [3, 4, 5, 2, 7, 1, 6, 3, 2, 5, 4, 4, 3, 2, 8, 1, 0, 9, 2, 2]
오픈프레임_점수계산(rolls)

# 문제 2: 스페어만 있는 경우
# 요구사항:
# 1프레임: 스페어(5,5), 2프레임: 3,0
# 1프레임 점수 = 5+5 +3 = 13
# 2프레임 점수 = 3+0 = 3 → 총합 16
# 함수에 [5][5][3] 입력 → 16 출력

def 스페어_점수계산(점수목록):
    총점 = 0
    i = 0
    프레임 = 1
    while i < len(점수목록) and 프레임 <= 10:
        # 스페어: 첫 투구가 10이 아니면서 두 투구 합이 10
        if 점수목록[i] < 10 and 점수목록[i] + 점수목록[i+1] == 10:
            프레임점수 = 10 + (점수목록[i+2] if i+2 < len(점수목록) else 0)
            print(f"{프레임}프레임(스페어): {프레임점수}")
            총점 += 프레임점수
            i += 2
        else:  # 오픈 프레임
            프레임점수 = 점수목록[i] + 점수목록[i+1]
            print(f"{프레임}프레임: {프레임점수}")
            총점 += 프레임점수
            i += 2
        프레임 += 1
    print("총점:", 총점)
    return 총점

# 예시 입력
rolls = [5, 5, 3, 0]
스페어_점수계산(rolls)


# 문제 3: 스트라이크 있는 경우
# 요구사항:
# 1프레임: 스트라이크(10), 2프레임: 3,2
# 1프레임 점수 = 10 +3+2 =15
# 2프레임 점수 =3+2=5 → 총합 20
# 입력 [10][3][2] → 출력 20
# 도전:
# 입력 [10][10][5][2]의 총점은?
# 정답: 10+10+5 (1프레임) +10+5+2 (2프레임) +5+2 (3프레임) = 25 +17 +7 = 49

def 스트라이크_점수계산(점수목록):
    총점 = 0
    투구위치 = 0
    프레임번호 = 1
    while 투구위치 < len(점수목록) and 프레임번호 <= 10:
        # 스트라이크: 첫 투구가 10핀
        if 점수목록[투구위치] == 10:
            프레임점수 = 10
            # 다음 두 투구 점수 더함 (보너스)
            if 투구위치 + 2 < len(점수목록):
                프레임점수 += 점수목록[투구위치+1] + 점수목록[투구위치+2]
            elif 투구위치 + 1 < len(점수목록):
                프레임점수 += 점수목록[투구위치+1]
            print(f"{프레임번호}프레임(스트라이크): {프레임점수}")
            총점 += 프레임점수
            투구위치 += 1
        # 스페어: 두 투구 합이 10, 첫 투구가 10이 아님
        elif 점수목록[투구위치] + 점수목록[투구위치+1] == 10:
            프레임점수 = 10
            if 투구위치 + 2 < len(점수목록):
                프레임점수 += 점수목록[투구위치+2]
            print(f"{프레임번호}프레임(스페어): {프레임점수}")
            총점 += 프레임점수
            투구위치 += 2
        # 오픈프레임: 두 투구 합이 10 미만
        else:
            프레임점수 = 점수목록[투구위치] + 점수목록[투구위치+1]
            print(f"{프레임번호}프레임: {프레임점수}")
            총점 += 프레임점수
            투구위치 += 2
        프레임번호 += 1
    print("총점:", 총점)
    return 총점

# 예시 입력
print("입력 [10, 3, 2]")
스트라이크_점수계산([10, 3, 2])  # 20

print("\n입력 [10, 10, 5, 2]")
스트라이크_점수계산([10, 10, 5, 2])  # 49

# 문제 5번: 10프레임 보너스 투구 처리
# 조건 요약
# 10번째 프레임에서 스트라이크를 치면 보너스 투구 2번
# 10번째 프레임에서 스페어를 치면 보너스 투구 1번
# 오픈프레임이면 추가 투구 없음
# 각 프레임별 점수 규칙은 기존과 동일
# 예시 입력
# 예시1: [10, 7, 2, 8, 1, 10, 10, 6, 2, 10, 10, 10, 8, 2,10]
# (마지막 프레임 스트라이크, 보너스 투구 2번 포함)

def 볼링_점수계산(점수목록):
    총점 = 0
    투구위치 = 0
    프레임번호 = 1

    while 프레임번호 <= 10:
      if 점수목록[투구위치] == 10:    #스트라이크일 경우
        프레임점수 = 10
        if 투구위치 +2 < len(점수목록):
          프레임점수 += 점수목록[투구위치+1] + 점수목록[투구위치+2]
        elif 투구위치 +1 < len(점수목록):
          프레임점수 += 점수목록[투구위치+1]
        총점 += 프레임점수
        투구위치 += 1
      elif 점수목록[투구위치] + 점수목록[투구위치+1] == 10:
        프레임점수 = 10
        if 투구위치 + 2 < len(점수목록):
          프레임점수 += 점수목록[투구위치+2]
          총점 += 프레임점수
          투구위치 += 2
      else:
        프레임점수 = 점수목록[투구위치] + 점수목록[투구위치+1]
        총점 += 프레임점수
        투구위치 += 2

      프레임번호 += 1

    print("총점:", 총점)
    return 총점

점수목록 = [10, 7, 2, 8, 1, 10, 10, 6, 2, 10, 10, 10, 8, 2, 10]
볼링_점수계산(점수목록)

# 문제 6번: 퍼펙트 게임 (300점)
# 요구사항
# 모든 프레임에서 스트라이크(10핀)를 쳤을 때,
# 총점이 얼마가 되는지 계산
# 입력: [10, 10, 10, 10, 10, 10, 10, 10, 10,[10][10][10] (12번 스트라이크)
# 정답: 300점
def 퍼펙트_게임(점수목록):
  총점 = 0
  투구위치 = 0
  프레임번호 = 1

  while 프레임번호 <= 10:
    if 점수목록[투구위치] == 10:
      프레임점수 = 10 + 점수목록[투구위치+1] + 점수목록[투구위치+2]
      총점 += 프레임점수
      투구위치 += 1

    프레임번호 += 1

  return 총점

