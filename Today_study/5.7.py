# 2단계: 여러 학생/학년/학기/과목 데이터 저장
# 문제
# 여러 명의 학생, 여러 학년/학기/과목의 점수를
# 중첩 딕셔너리로 반복문을 이용해 저장하세요.
# 예시:
# "홍길동", 1학년 1학기 국어 88점
# "홍길동", 1학년 1학기 영어 92점
# "이영희", 2학년 2학기 수학 77점
# 저장 후 성적표 딕셔너리를 출력하세요.


성적표 = {}
데이터 = [
    ("홍길동", "1학년", "1학기", "국어", 88),
    ("홍길동", "1학년", "1학기", "영어", 92),
    ("홍길동", "1학년", "2학기", "수학", 77),
    ("이영희", "2학년", "1학기", "국어", 85),
    ("이영희", "2학년", "2학기", "영어", 90)
]
for 이름, 학년, 학기, 과목, 점수 in 데이터:
  if 이름 not in 성적표:
    성적표[이름] = {}
  if 학년 not in 성적표[이름]:
    성적표[이름][학년] = {}
  if 학기 not in 성적표[이름][학년]:
    성적표[이름][학년][학기] = {}
  성적표[이름][학년][학기][과목] = 점수

print(성적표)

# 3단계: 성적표에서 학생별 전체 평균 구하기
#문제: 위에서 만든 성적표 딕셔너리에서
#특정 학생의 모든 과목 점수의 평균을 구하세요.

성적표 = {}
데이터 = [
    ("홍길동", "1학년", "1학기", "국어", 88),
    ("홍길동", "1학년", "1학기", "영어", 92),
    ("홍길동", "1학년", "2학기", "수학", 77),
    ("이영희", "2학년", "1학기", "국어", 85),
    ("이영희", "2학년", "2학기", "영어", 90)
]

이름 = "홍길동"
총점 = 0
과목수 = 0

for 학년 in 성적표[이름]:
  for 학기 in 성적표[이름][학년]:
    for 과목 in 성적표[이름][학년][학기]:
      점수 = 성적표[이름][학년][학기][과목]
      총점 += 점수
      과목수 += 1

if 과목수 > 0:
  평균 = 총점 / 과목수
print(f"{이름}의 평균점수: {평균:.2f}")